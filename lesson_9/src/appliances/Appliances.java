package appliances;

public abstract class Appliances {
    private int price;
    private String brand;

    public abstract void switchOn();

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}
